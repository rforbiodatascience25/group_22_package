## ----include = FALSE----------------------------------------------------------
knitr::opts_chunk$set(
  collapse = TRUE,
  comment = "#>"
)

## ----setup--------------------------------------------------------------------
library("cdogmag22")

## -----------------------------------------------------------------------------
set.seed(22160)
dna <- generate_dna_sequence(200)
print(dna)

## -----------------------------------------------------------------------------
rna <- DNA_to_RNA(dna)
print(rna)

## -----------------------------------------------------------------------------
codons <- codon(rna,
                start = 1)
print(codons)

## -----------------------------------------------------------------------------
amino <- codon_to_amino_acids(codons)
print(amino)

## -----------------------------------------------------------------------------
plot_char_counts(amino)

## -----------------------------------------------------------------------------
set.seed(22160)
aminoplot <- generate_dna_sequence(200) |>
  DNA_to_RNA() |>
    codon(start = 1) |>
      codon_to_amino_acids() |>
        plot_char_counts()
print(aminoplot)

